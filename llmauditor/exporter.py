def export_execution(
    data: dict[str, Any],
    quality: dict[str, Any],
    fmt: str,
    output_dir: str = ".",
) -> str:
    """Export a single execution audit report."""
    try:
        return _export_execution_impl(data, quality, fmt, output_dir)
    except Exception as exc:
        return f"Export failed: {exc}"


def _export_execution_impl(
    data: dict[str, Any],
    quality: dict[str, Any],
    fmt: str,
    output_dir: str = ".",
) -> str:
    """Internal export logic (separated for error isolation)."""
    fmt = fmt.lower().strip()
    if fmt not in ("md", "html", "pdf"):
        raise ValueError(f"Unsupported format: '{fmt}'")

    os.makedirs(output_dir, exist_ok=True)
    integrity_hash = _hash(data)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    eid_short = str(data.get("execution_id", "unknown"))[:8]
    base = f"audit_{eid_short}_{ts}"

    if fmt == "md":
        content = _exec_md(data, quality, integrity_hash)
        path = os.path.join(output_dir, f"{base}.md")
        _write(path, content)
    elif fmt == "html":
        content = _exec_html(data, quality, integrity_hash)
        path = os.path.join(output_dir, f"{base}.html")
        _write(path, content)
    else:
        path = os.path.join(output_dir, f"{base}.pdf")
        _exec_pdf(data, quality, integrity_hash, path)

    return os.path.abspath(path)


# ── Execution: Markdown ───────────────────────────────────────────────────── #

def _exec_md(data: dict, quality: dict, ihash: str) -> str:
    ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    conf = quality.get("confidence_score", "N/A")
    risk = quality.get("risk_level", "N/A")
    notes = quality.get("notes", [])
    summary = quality.get("summary", "")

    lines = [
        "# AI Execution Audit Report", "",
        f"> Generated by **{_VERSION}**", "", "---", "",
        "## Cover", "",
        "| Field | Value |", "|---|---|",
        f"| Execution ID | `{data.get('execution_id', '')}` |",
        f"| Model | {data.get('model_name', '')} |",
        f"| Date & Time | {ts} |",
        f"| Generated By | {_VERSION} |",
        "", "---", "", "## Executive Summary", "", summary,
        "", "---", "", "## Execution Overview", "",
        "| Metric | Value |", "|---|---|",
        f"| Model | {data.get('model_name', '')} |",
        f"| Execution Time | {data.get('execution_time', '')} sec |",
        f"| Input Tokens | {data.get('input_tokens', 0):,} |",
        f"| Output Tokens | {data.get('output_tokens', 0):,} |",
        f"| Total Tokens | {data.get('total_tokens', 0):,} |",
        f"| Estimated Cost | ${data.get('estimated_cost', 0):.6f} USD |",
        f"| Confidence | {conf}% |",
        f"| Risk Level | {risk} |",
        "", "---", "", "## Quality & Risk Assessment", "",
    ]
    for n in notes:
        lines.append(f"- {n}")

    # Hallucination section
    hal = data.get("hallucination")
    if hal:
        lines += ["", "---", "", "## Hallucination Analysis", "",
                   "| Metric | Value |", "|---|---|",
                   f"| Risk Score | {hal.get('risk_score_pct', 'N/A')}% ({hal.get('risk_level', 'N/A')}) |",
                   f"| Method | {hal.get('method', 'N/A')} |",
                   f"| Factual Claims | {hal.get('factual_claims_count', 0)} |",
                   f"| Hedging Ratio | {hal.get('hedging_ratio', 0):.2%} |",
                   f"| Absolute Claims | {hal.get('absolute_claims_count', 0)} |"]

    lines += ["", "---", "",
              f"**Integrity Hash:** `{ihash}`", "",
              f"*Generated at {ts} by {_VERSION}*"]

    return "\n".join(lines)


