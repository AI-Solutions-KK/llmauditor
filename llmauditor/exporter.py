def _build_plain_summary(r: "EvaluationReport") -> list[dict]:
    """
    Generate a list of plain-language summary bullets from evaluation data.

    Each item is a dict: {"icon": str, "heading": str, "text": str}
    Written so any non-technical reader can understand what happened.
    """
    s = r.score
    m = r.metrics
    sess = r.session
    items: list[dict] = []

    # ── What was tested ───────────────────────────────────────────────────── #
    items.append({
        "icon": "\U0001f4cb",  # clipboard
        "heading": "What Was Tested",
        "text": (
            f"\"{sess.app_name}\" (version {sess.version}) was tested "
            f"{m.total_runs} time{'s' if m.total_runs != 1 else ''} "
            f"using the {', '.join(m.models_used) or 'AI'} model."
        ),
    })

    # ── Overall verdict ───────────────────────────────────────────────────── #
    if s.overall >= 90:
        verdict_text = (
            f"The application received a top-tier \"{s.level}\" certification "
            f"with a score of {s.overall:.0f} out of 100. "
            "This means it performed excellently across all quality checks."
        )
    elif s.overall >= 80:
        verdict_text = (
            f"The application earned a \"{s.level}\" certification "
            f"({s.overall:.0f}/100). It performed well overall with only "
            "minor areas for improvement."
        )
    elif s.overall >= 70:
        verdict_text = (
            f"The application received a \"{s.level}\" certification "
            f"({s.overall:.0f}/100). Performance is acceptable but several "
            "areas need attention before production use."
        )
    elif s.overall >= 60:
        verdict_text = (
            f"The application received a \"{s.level}\" rating "
            f"({s.overall:.0f}/100). Significant improvements are needed "
            "before it can be fully trusted in production."
        )
    else:
        verdict_text = (
            f"The application did not pass certification "
            f"({s.overall:.0f}/100). Major issues were found that must be "
            "resolved before production deployment."
        )
    items.append({"icon": s.level_emoji, "heading": "Overall Result", "text": verdict_text})

    # ── Reliability / Stability ───────────────────────────────────────────── #
    stab = s.subscores.get("stability", 0)
    if stab >= 90:
        stab_text = "The AI responded consistently every time it was called. Response times and output lengths were steady and predictable."
    elif stab >= 70:
        stab_text = "Responses were mostly consistent, though some variation was observed in speed or output length. Generally dependable."
    else:
        stab_text = "Responses varied noticeably between calls. Speed and output length were unpredictable, which may affect user experience."
    fail_note = ""
    if m.failure_rate > 0:
        fail_note = f" {m.failure_rate:.0%} of calls failed to complete."
    items.append({"icon": "\u26a1", "heading": "Reliability", "text": stab_text + fail_note})

    # ── Accuracy / Truthfulness ───────────────────────────────────────────── #
    fact = s.subscores.get("factual_reliability", 0)
    hal_mean = m.hallucination_stats.mean if m.hallucination_stats.count > 0 else 0
    if fact >= 90:
        fact_text = "The AI's answers showed very low risk of containing made-up or inaccurate information."
    elif fact >= 70:
        fact_text = "The AI's answers were mostly reliable, but some responses contained statements that could not be fully verified."
    else:
        fact_text = "There is a noticeable risk that the AI may produce inaccurate or fabricated information. Extra fact-checking is recommended."
    if m.hallucination_stats.count > 0:
        fact_text += f" Average accuracy risk level: {hal_mean:.0%}."
    items.append({"icon": "\U0001f3af", "heading": "Accuracy", "text": fact_text})

    # ── Safety & Rules ────────────────────────────────────────────────────── #
    gov = s.subscores.get("governance_compliance", 0)
    if gov >= 95 and m.guard_violations == 0 and m.budget_violations == 0:
        gov_text = "The AI followed all safety rules and budget limits perfectly. No violations were detected."
    elif gov >= 70:
        parts = []
        if m.guard_violations > 0:
            parts.append(f"{m.guard_violations} quality gate violation{'s' if m.guard_violations != 1 else ''}")
        if m.budget_violations > 0:
            parts.append(f"{m.budget_violations} budget limit breach{'es' if m.budget_violations != 1 else ''}")
        gov_text = "Minor safety or budget issues were detected: " + ", ".join(parts) + ". These should be reviewed."
    else:
        gov_text = "Multiple safety or budget violations occurred. The system's guardrails may need to be reconfigured."
    items.append({"icon": "\U0001f6e1", "heading": "Safety & Rules", "text": gov_text})

    # ── Cost ──────────────────────────────────────────────────────────────── #
    total_cost = m.cost_stats.total
    avg_cost = m.cost_stats.mean
    if total_cost == 0:
        cost_text = "No cost was incurred during testing (free tier or zero-cost model)."
    elif avg_cost < 0.001:
        cost_text = f"Total cost for all {m.total_runs} calls was ${total_cost:.4f} USD — very economical."
    elif avg_cost < 0.01:
        cost_text = f"Total cost: ${total_cost:.4f} USD (average ${avg_cost:.4f} per call). Cost is reasonable."
    else:
        cost_text = f"Total cost: ${total_cost:.4f} USD (average ${avg_cost:.4f} per call). Consider optimising prompts or using a cheaper model to reduce expenses."
    items.append({"icon": "\U0001f4b0", "heading": "Cost", "text": cost_text})

    # ── Suggestions (simplified) ──────────────────────────────────────────── #
    if r.suggestions:
        critical = [sg for sg in r.suggestions if sg.severity in ("critical", "high")]
        if critical:
            titles = ", ".join(sg.title.lower() for sg in critical[:3])
            items.append({
                "icon": "\u26a0\ufe0f",
                "heading": "Action Needed",
                "text": f"The evaluation found {len(critical)} important issue{'s' if len(critical) != 1 else ''} to address: {titles}.",
            })
    else:
        items.append({
            "icon": "\u2705",
            "heading": "No Issues Found",
            "text": "All quality checks passed. No immediate actions are required.",
        })

    return items


