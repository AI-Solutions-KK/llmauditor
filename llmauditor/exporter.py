def _exec_pdf(data: dict, quality: dict, ihash: str, path: str) -> None:
    rl = _import_reportlab()
    ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    conf = quality.get("confidence_score", "N/A")
    risk = quality.get("risk_level", "N/A")
    notes = quality.get("notes", [])
    summary = quality.get("summary", "")

    doc, styles, story, colors_map = _pdf_base(path, "AI Execution Audit Report", ts)

    story += _pdf_section("Cover", styles)
    story.append(_pdf_table([
        ["Field", "Value"],
        ["Execution ID", str(data.get("execution_id", ""))],
        ["Model", str(data.get("model_name", ""))],
        ["Date & Time", ts],
        ["Generated By", _VERSION],
    ], colors_map))
    story.append(rl["Spacer"](1, 4 * rl["mm"]))

    story += _pdf_section("Executive Summary", styles)
    story.append(rl["Paragraph"](summary, styles["summary"]))
    story.append(rl["Spacer"](1, 4 * rl["mm"]))

    story += _pdf_section("Execution Overview", styles)
    story.append(_pdf_table([
        ["Metric", "Value"],
        ["Model", str(data.get("model_name", ""))],
        ["Execution Time", f"{data.get('execution_time', '')} sec"],
        ["Input Tokens", f"{data.get('input_tokens', 0):,}"],
        ["Output Tokens", f"{data.get('output_tokens', 0):,}"],
        ["Total Tokens", f"{data.get('total_tokens', 0):,}"],
        ["Estimated Cost", f"${data.get('estimated_cost', 0):.6f} USD"],
        ["Confidence", f"{conf}%"],
        ["Risk Level", str(risk)],
    ], colors_map))
    story.append(rl["Spacer"](1, 4 * rl["mm"]))

    story += _pdf_section("Quality & Risk Assessment", styles)
    for note in notes:
        story.append(rl["Paragraph"](f"\u2022 {note}", styles["body"]))

    # Hallucination section
    hal = data.get("hallucination")
    if hal:
        story.append(rl["Spacer"](1, 4 * rl["mm"]))
        story += _pdf_section("Hallucination Analysis", styles)
        story.append(_pdf_table([
            ["Metric", "Value"],
            ["Risk Score", f"{hal.get('risk_score_pct', 'N/A')}% ({hal.get('risk_level', 'N/A')})"],
            ["Method", str(hal.get("method", "N/A"))],
            ["Factual Claims", str(hal.get("factual_claims_count", 0))],
            ["Hedging Ratio", f"{hal.get('hedging_ratio', 0):.2%}"],
        ], colors_map))

    # Integrity hash
    story.append(rl["Spacer"](1, 6 * rl["mm"]))
    story.append(rl["HRFlowable"](width="100%", thickness=0.5,
                 color=colors_map["LIGHT_GREY"], spaceAfter=4, spaceBefore=6))
    story.append(rl["Paragraph"](
        f"<font size='7' color='#7f8c8d'><b>Integrity Hash:</b> {ihash}</font>",
        styles["body"]))

    doc.build(story, onFirstPage=_pdf_page_handler(ts, "AI Execution Audit Report"),
              onLaterPages=_pdf_page_handler(ts, "AI Execution Audit Report"))


