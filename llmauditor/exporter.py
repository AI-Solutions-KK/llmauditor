def _import_reportlab() -> dict:
    """Import reportlab components. Returns a dict for easy access."""
    try:
        from reportlab.lib import colors
        from reportlab.lib.pagesizes import A4
        from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
        from reportlab.lib.units import mm
        from reportlab.platypus import (
            SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
            HRFlowable, KeepTogether,
        )
        from reportlab.lib.enums import TA_CENTER, TA_LEFT
    except ImportError as exc:
        raise ImportError(
            "reportlab is required for PDF export. Install: pip install reportlab>=4.0.0"
        ) from exc

    return {
        "colors": colors, "A4": A4, "mm": mm,
        "SimpleDocTemplate": SimpleDocTemplate, "Paragraph": Paragraph,
        "Spacer": Spacer, "Table": Table, "TableStyle": TableStyle,
        "HRFlowable": HRFlowable, "KeepTogether": KeepTogether,
        "ParagraphStyle": ParagraphStyle, "getSampleStyleSheet": getSampleStyleSheet,
        "TA_CENTER": TA_CENTER, "TA_LEFT": TA_LEFT,
    }


