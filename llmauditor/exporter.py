def _generate_cert_number(ihash: str) -> str:
    """Generate a unique certificate number from integrity hash and timestamp."""
    ts = datetime.now().strftime("%Y%m%d")
    return f"LMA-{ts}-{ihash[:6].upper()}"


def export_certification(report: "EvaluationReport", fmt: str, output_dir: str = ".") -> str:
    """Export a full evaluation certification report."""
    try:
        return _export_certification_impl(report, fmt, output_dir)
    except Exception as exc:
        return f"Export failed: {exc}"


def _export_certification_impl(report: "EvaluationReport", fmt: str, output_dir: str = ".") -> str:
    """Internal certification export logic (separated for error isolation)."""
    fmt = fmt.lower().strip()
    if fmt not in ("md", "html", "pdf"):
        raise ValueError(f"Unsupported format: '{fmt}'")

    os.makedirs(output_dir, exist_ok=True)
    ihash = _hash(report.to_dict())
    cert_number = _generate_cert_number(ihash)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    base = f"certification_{report.session.app_name.replace(' ', '_')}_{ts}"

    if fmt == "md":
        content = _cert_md(report, ihash, cert_number)
        path = os.path.join(output_dir, f"{base}.md")
        _write(path, content)
    elif fmt == "html":
        content = _cert_html(report, ihash, cert_number)
        path = os.path.join(output_dir, f"{base}.html")
        _write(path, content)
    else:
        path = os.path.join(output_dir, f"{base}.pdf")
        _cert_pdf(report, ihash, cert_number, path)

    return os.path.abspath(path)


def export_certification_all(report: "EvaluationReport", output_dir: str = ".") -> dict[str, str]:
    """
    Export the certification report in all three formats (MD, HTML, PDF).

    Returns a dict mapping format name to absolute file path:
        {"md": "/path/to.md", "html": "/path/to.html", "pdf": "/path/to.pdf"}
    """
    os.makedirs(output_dir, exist_ok=True)
    ihash = _hash(report.to_dict())
    cert_number = _generate_cert_number(ihash)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    base = f"certification_{report.session.app_name.replace(' ', '_')}_{ts}"
    results: dict[str, str] = {}

    # Markdown
    try:
        md_content = _cert_md(report, ihash, cert_number)
        md_path = os.path.join(output_dir, f"{base}.md")
        _write(md_path, md_content)
        results["md"] = os.path.abspath(md_path)
    except Exception as exc:
        results["md"] = f"ERROR: {exc}"

    # HTML
    try:
        html_content = _cert_html(report, ihash, cert_number)
        html_path = os.path.join(output_dir, f"{base}.html")
        _write(html_path, html_content)
        results["html"] = os.path.abspath(html_path)
    except Exception as exc:
        results["html"] = f"ERROR: {exc}"

    # PDF
    try:
        pdf_path = os.path.join(output_dir, f"{base}.pdf")
        _cert_pdf(report, ihash, cert_number, pdf_path)
        results["pdf"] = os.path.abspath(pdf_path)
    except Exception as exc:
        results["pdf"] = f"ERROR: {exc}"

    return results


# ── Certification: Markdown ───────────────────────────────────────────────── #

def _cert_md(r: "EvaluationReport", ihash: str, cert_number: str) -> str:
    ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    s = r.score
    m = r.metrics
    sess = r.session

    # Mode label
    mode_raw = getattr(sess, "mode", "simulated") or "simulated"
    is_live = mode_raw.lower() in ("live", "api", "real")
    mode_label = "With API Call" if is_live else "Without API Call"

    lines = [
        "# GenAI Application Certification Report", "",
        f"> Generated by **{_VERSION}**", "",
        "---", "",
        '<div align="center">', "",
        "**\u2605 CERTIFIED \u2605**", "",
        f"### \U0001f3c6 {s.level.upper()} \U0001f3c6", "",
        f"\U0001f4cb **Certificate No:** `{cert_number}`", "",
        f"*Issued by {_VERSION} Certification Authority*", "",
        "</div>", "",
        "---", "",

        # Section 1 — Application Identity
        "## 1. Application Identity", "",
        "| Field | Value |", "|---|---|",
        f"| Application | {sess.app_name} |",
        f"| Version | {sess.version} |",
        f"| Evaluation Mode | {mode_label} |",
        f"| Evaluation Period | {sess.start_time:%Y-%m-%d %H:%M} → {(sess.end_time or datetime.now()):%Y-%m-%d %H:%M} |",
        f"| Duration | {sess.duration_seconds:.1f}s |",
        f"| Total Executions | {m.total_runs} |",
        f"| Models Used | {', '.join(m.models_used) or 'N/A'} |",
        "", "---", "",

        # Section 2 — Evaluation Summary
        "## 2. Evaluation Summary", "",
        f"**Overall Score: {s.overall:.1f}/100**", "",
        f"**Certification Level: {s.level_emoji} {s.level}**", "",
        "", "---", "",

        # Section 3 — Metrics & Distributions
        "## 3. Metrics & Distributions", "",
        "### Token Usage", "",
        "| Metric | Mean | StdDev | Min | Max |", "|---|---|---|---|---|",
        f"| Total Tokens | {m.token_stats.mean:.0f} | {m.token_stats.stddev:.0f} | {m.token_stats.min_val:.0f} | {m.token_stats.max_val:.0f} |",
        f"| Input Tokens | {m.input_token_stats.mean:.0f} | {m.input_token_stats.stddev:.0f} | {m.input_token_stats.min_val:.0f} | {m.input_token_stats.max_val:.0f} |",
        f"| Output Tokens | {m.output_token_stats.mean:.0f} | {m.output_token_stats.stddev:.0f} | {m.output_token_stats.min_val:.0f} | {m.output_token_stats.max_val:.0f} |",
        "",
        "### Cost", "",
        "| Metric | Value |", "|---|---|",
        f"| Total Cost | ${m.cost_stats.total:.6f} |",
        f"| Mean Cost | ${m.cost_stats.mean:.6f} |",
        f"| StdDev | ${m.cost_stats.stddev:.6f} |",
        "",
        "### Latency", "",
        f"| Mean | {m.latency_stats.mean:.3f}s | StdDev | {m.latency_stats.stddev:.3f}s | Max | {m.latency_stats.max_val:.3f}s |",
        "", "---", "",

        # Section 4 — Hallucination Analysis
        "## 4. Hallucination Analysis", "",
    ]

    if m.hallucination_stats.count > 0:
        h = m.hallucination_stats
        lines += [
            "| Metric | Value |", "|---|---|",
            f"| Mean Risk | {h.mean:.1%} |",
            f"| Max Risk | {h.max_val:.1%} |",
            f"| Min Risk | {h.min_val:.1%} |",
            f"| Executions Analysed | {h.count} |",
        ]
    else:
        lines.append("*No hallucination analysis data available.*")

    lines += ["", "---", "",

        # Section 5 — Governance Compliance
        "## 5. Governance Compliance", "",
        "| Metric | Value |", "|---|---|",
        f"| Guard Violations | {m.guard_violations} |",
        f"| Budget Violations | {m.budget_violations} |",
        f"| Total Warnings | {m.total_warnings} |",
        "", "---", "",

        # Section 6 — Stability Analysis
        "## 6. Stability Analysis", "",
        f"- Failure Rate: {m.failure_rate:.1%}",
        f"- Latency CV: {m.latency_stats.stddev / max(m.latency_stats.mean, 0.001):.3f}",
        f"- Token CV: {m.token_stats.stddev / max(m.token_stats.mean, 1):.3f}",
        "", "---", "",

        # Section 7 — Scoring Breakdown
        "## 7. Scoring Breakdown", "",
        "| Subscore | Value | Weight |", "|---|---|---|",
    ]

    for name, val in s.subscores.items():
        w = s.weights.get(name, 0)
        lines.append(f"| {name.replace('_', ' ').title()} | {val:.1f} | {w:.0%} |")

    lines += ["", "---", "",

        # Section 8 — Certification Verdict
        "## 8. Certification Verdict", "",
        f"### {s.level_emoji} {s.level}  —  {s.overall:.1f}/100", "",
        "", "---", "",

        # Section 9 — Improvement Recommendations
        "## 9. Improvement Recommendations", "",
    ]

    if r.suggestions:
        for sg in r.suggestions:
            lines.append(f"- **[{sg.severity.upper()}] {sg.title}**: {sg.detail}")
    else:
        lines.append("*No improvement recommendations — all metrics are within acceptable thresholds.*")

    lines += ["", "---", "",

        # Section 10 — Integrity Hash
        "## 10. Integrity Hash", "",
        f"`{ihash}`", "",
        "", "---", "",

        # Section 11 — Understanding the Numbers
        "## 11. Understanding the Numbers", "",
        "> **What do the statistics above mean?** Here is a detailed "
        "explanation for non-technical readers.", "",
    ]

    for item in _build_detailed_explanation(r):
        lines.append(f"### {item['term']}")
        lines.append(f"")
        lines.append(f"**Value:** {item['value']}")
        lines.append(f"")
        lines.append(f"{item['explanation']}")
        lines.append(f"")

    lines += [
        "---", "",

        # Section 12 — Plain Language Summary
        "## 12. Report Summary", "",
        "> **What does this report mean?** Here is a simple summary "
        "anyone can understand.", "",
    ]

    for item in _build_plain_summary(r):
        lines.append(f"### {item['icon']}  {item['heading']}")
        lines.append(f"")
        lines.append(f"{item['text']}")
        lines.append(f"")

    # ── Digital Signature ──────────────────────────────────────────────── #
    lines += [
        "---", "",
        "## \u270d\ufe0f Digital Signature", "",
        '<div align="center">', "",
        "| Field | Value |", "|:---|:---|",
        f"| **Certificate No** | `{cert_number}` |",
        f"| **Issued By** | {_VERSION} \u2014 Automated Certification Engine |",
        f"| **Issue Date** | {ts} |",
        f"| **Integrity Hash** | `{ihash}` |", "",
        "---", "",
        "*This certificate was digitally signed and generated automatically*  ",
        f"*by the {_VERSION} Certification Engine.*  ",
        "*No human intervention was involved in the evaluation process.*", "",
        "</div>", "",
    ]

    lines += [
        "---", "",
        f"*Generated at {ts} by {_VERSION}*",
    ]

    return "\n".join(lines)


