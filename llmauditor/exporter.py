    s = r.score
    m = r.metrics
    sess = r.session

    level_colors = {"Platinum": "#00d4ff", "Gold": "#f1c40f", "Silver": "#bdc3c7",
                    "Conditional Pass": "#e67e22", "Fail": "#e74c3c"}
    lcolor = level_colors.get(s.level, "#95a5a6")

    suggestions_html = ""
    if r.suggestions:
        for sg in r.suggestions:
            sc = {"critical": "#e74c3c", "high": "#e67e22", "medium": "#f1c40f", "low": "#95a5a6"}
            c = sc.get(sg.severity, "#95a5a6")
            suggestions_html += f'<li><span class="badge" style="background:{c}">{sg.severity.upper()}</span> <strong>{sg.title}</strong>: {sg.detail}</li>'
    else:
        suggestions_html = "<li>No improvement recommendations needed.</li>"

    subscores_html = ""
    for name, val in s.subscores.items():
        sc = "#27ae60" if val >= 80 else "#f39c12" if val >= 60 else "#e74c3c"
        w = s.weights.get(name, 0)
        subscores_html += f'<tr><td>{name.replace("_", " ").title()}</td><td><span class="badge" style="background:{sc}">{val:.1f}</span></td><td>{w:.0%}</td></tr>'

