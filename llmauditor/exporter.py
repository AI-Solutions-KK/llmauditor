def _pdf_page_handler(ts: str, title: str, header_color: str = "#1a253c",
                      cert_number: str | None = None, level: str | None = None):
    """Return a function for reportlab page events (header/footer drawing)."""
    def _handler(canvas, doc):
        from reportlab.lib.pagesizes import A4
        from reportlab.lib.units import mm
        from reportlab.lib import colors

        PAGE_W, PAGE_H = A4
        margin = 18 * mm

        DARK = colors.HexColor(header_color)
        BG_GREY = colors.HexColor("#f4f6f9")
        MID_GREY = colors.HexColor("#7f8c8d")
        LIGHT_GREY = colors.HexColor("#eaecf0")

        canvas.saveState()

        # Header bar
        canvas.setFillColor(DARK)
        canvas.rect(0, PAGE_H - 28 * mm, PAGE_W, 28 * mm, fill=1, stroke=0)
        canvas.setFillColor(colors.white)
        canvas.setFont("Helvetica-Bold", 13)
        canvas.drawCentredString(PAGE_W / 2, PAGE_H - 14 * mm, title)
        canvas.setFont("Helvetica", 8)
        canvas.setFillColor(colors.HexColor("#a0b0c8"))
        canvas.drawCentredString(PAGE_W / 2, PAGE_H - 21 * mm, f"{_VERSION}  |  {ts}")

        # Certification stamp (first page only)
        if cert_number and level:
            _draw_pdf_stamp(canvas, PAGE_W, PAGE_H, mm, cert_number, level)

        # Footer
        canvas.setFillColor(BG_GREY)
        canvas.rect(0, 0, PAGE_W, 18 * mm, fill=1, stroke=0)
        canvas.setFillColor(MID_GREY)
        canvas.setFont("Helvetica", 7)
        canvas.drawString(margin, 11 * mm, _VERSION)
        canvas.drawRightString(PAGE_W - margin, 11 * mm, f"Page {doc.page}")

        canvas.restoreState()

    return _handler


def _draw_pdf_stamp(canvas, PAGE_W, PAGE_H, mm, cert_number, level):
    """Draw a circular certification stamp on the PDF header area."""
    from reportlab.lib import colors

    GREEN = colors.HexColor("#0a4a23")
    WHITE = colors.white

    # Position: right side of header — enlarged for A4 readability
    cx = PAGE_W - 30 * mm
    cy = PAGE_H - 14 * mm
    r = 18 * mm

    canvas.saveState()

    # White filled background circle
    canvas.setFillColor(WHITE)
    canvas.setStrokeColor(GREEN)
    canvas.setLineWidth(2.8)
    canvas.circle(cx, cy, r, stroke=1, fill=1)

    # Inner circle
    canvas.setStrokeColor(GREEN)
    canvas.setLineWidth(1.2)
    canvas.circle(cx, cy, r * 0.80, stroke=1, fill=0)

    # Decorative dots at cardinal points between rings
    dot_r = r * 0.90
    canvas.setFillColor(GREEN)
    for angle_deg in [0, 90, 180, 270]:
        angle_rad = math.radians(angle_deg)
        dx = cx + dot_r * math.cos(angle_rad)
        dy = cy + dot_r * math.sin(angle_rad)
        canvas.circle(dx, dy, 0.9 * mm, stroke=0, fill=1)

    # Top arc text: CERTIFIED
    _draw_arc_text_on_canvas(
        canvas, cx, cy, r * 0.88, "CERTIFIED",
        center_angle_deg=90, char_gap_deg=14,
        facing="out", font="Helvetica-Bold", size=7.8, color=GREEN)

    # Bottom arc text: LLM AUDITOR
    _draw_arc_text_on_canvas(
        canvas, cx, cy, r * 0.88, "LLM AUDITOR",
        center_angle_deg=270, char_gap_deg=10.5,
        facing="in", font="Helvetica-Bold", size=6.8, color=GREEN)

    # Center: certification level
    canvas.setFillColor(GREEN)
    canvas.setFont("Helvetica-Bold", 9)
    canvas.drawCentredString(cx, cy + 2.5, level.upper())

    # Decorative line under level
    line_w = r * 0.44
    canvas.setStrokeColor(GREEN)
    canvas.setLineWidth(0.5)
    canvas.line(cx - line_w, cy - 1.5, cx + line_w, cy - 1.5)

    # Certificate number
    canvas.setFont("Helvetica", 5.5)
    canvas.drawCentredString(cx, cy - 6.5, cert_number)

    canvas.restoreState()


def _draw_arc_text_on_canvas(canvas, cx, cy, radius, text, center_angle_deg,
                              char_gap_deg, facing, font, size, color):
    """Draw text along a circular arc on reportlab canvas."""
    n = len(text)
    total_gap = (n - 1) * char_gap_deg

    canvas.saveState()
    canvas.setFillColor(color)

    for i, ch in enumerate(text):
        if facing == "out":
            # Top arc: text reads left to right, characters face outward
            angle_deg = center_angle_deg + total_gap / 2 - i * char_gap_deg
            rot = angle_deg - 90
        else:
            # Bottom arc: text reads left to right, characters face inward
            angle_deg = center_angle_deg - total_gap / 2 + i * char_gap_deg
            rot = angle_deg + 90

        angle_rad = math.radians(angle_deg)
        x = cx + radius * math.cos(angle_rad)
        y = cy + radius * math.sin(angle_rad)

        canvas.saveState()
        canvas.translate(x, y)
        canvas.rotate(rot)
        canvas.setFont(font, size)
        canvas.drawCentredString(0, 0, ch)
        canvas.restoreState()

    canvas.restoreState()
