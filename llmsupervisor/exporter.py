"""
exporter.py — Audit Export Engine for LLMSupervisor.

Responsibilities:
- Accept a structured data dict (from ExecutionReport.to_dict())
- Accept quality signals dict (confidence, risk, notes, summary)
- Generate Markdown audit document
- Convert Markdown data to HTML audit document
- Generate boardroom-ready PDF from HTML/data using reportlab
- Compute SHA-256 integrity hash of execution data
- Write output files to disk

Design:
- All export formats derive from a single internal data model
- Markdown is the base template layer
- HTML is generated from the same data model
- PDF is generated natively via reportlab (no HTML-to-PDF conversion)
- No rich / CLI formatting used here — clean document output only
- no print() calls — raises exceptions on failure
"""

import hashlib
import json
import os
import textwrap
from datetime import datetime
from typing import Any


# ── Public entry point ────────────────────────────────────────────────────── #

def export(
    data: dict[str, Any],
    quality: dict[str, Any],
    fmt: str,
    output_dir: str = ".",
) -> str:
    """
    Export an execution audit report in the requested format.

    Args:
        data:       ExecutionReport.to_dict() output.
        quality:    Dict with keys: confidence_score, risk_level, notes (list), summary (str).
        fmt:        One of: 'md', 'html', 'pdf'
        output_dir: Directory to write the output file. Defaults to current dir.

    Returns:
        Absolute path to the generated file.

    Raises:
        ValueError: If fmt is not a supported format.
        OSError:    If the output directory cannot be created or file cannot be written.
    """
    fmt = fmt.lower().strip()
    if fmt not in ("md", "html", "pdf"):
        raise ValueError(f"Unsupported export format: '{fmt}'. Choose from: md, html, pdf")

    os.makedirs(output_dir, exist_ok=True)

    integrity_hash = _compute_integrity_hash(data)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    execution_id_short = str(data.get("execution_id", "unknown"))[:8]
    base_name = f"audit_{execution_id_short}_{timestamp}"

    if fmt == "md":
        content = _build_markdown(data, quality, integrity_hash)
        out_path = os.path.join(output_dir, f"{base_name}.md")
        _write_text(out_path, content)

    elif fmt == "html":
        content = _build_html(data, quality, integrity_hash)
        out_path = os.path.join(output_dir, f"{base_name}.html")
        _write_text(out_path, content)

    elif fmt == "pdf":
        out_path = os.path.join(output_dir, f"{base_name}.pdf")
        _build_pdf(data, quality, integrity_hash, out_path)

    return os.path.abspath(out_path)


# ── Integrity hash ─────────────────────────────────────────────────────────── #

def _compute_integrity_hash(data: dict[str, Any]) -> str:
    """
    Compute a SHA-256 hash of the execution data dict.

    Serialises the dict to compact, sorted JSON for determinism.
    Returns first 16 hex characters for display (full hash in PDF footer).
    """
    payload = json.dumps(data, sort_keys=True, default=str).encode("utf-8")
    return hashlib.sha256(payload).hexdigest()


# ── Markdown builder ───────────────────────────────────────────────────────── #

def _build_markdown(
    data: dict[str, Any],
    quality: dict[str, Any],
    integrity_hash: str,
) -> str:
    """Build a complete Markdown audit report string."""
    generated_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    confidence = quality.get("confidence_score", "N/A")
    risk = quality.get("risk_level", "N/A")
    notes: list[str] = quality.get("notes", [])
    summary: str = quality.get("summary", "")

    lines = [
        "# AI Execution Audit Report",
        "",
        "> Generated by **LLMSupervisor v0.1.0**",
        "",
        "---",
        "",
        "## Cover",
        "",
        f"| Field              | Value |",
        f"|-------------------|-------|",
        f"| Report Title       | AI Execution Audit Report |",
        f"| Application        | LLMSupervisor |",
        f"| Execution ID       | `{data.get('execution_id', '')}` |",
        f"| Date & Time        | {generated_at} |",
        f"| Model Used         | {data.get('model_name', '')} |",
        f"| Generated By       | LLMSupervisor v0.1.0 |",
        "",
        "---",
        "",
        "## Executive Summary",
        "",
        summary,
        "",
        "---",
        "",
        "## Execution Overview",
        "",
        "| Metric            | Value |",
        "|-------------------|-------|",
        f"| Model             | {data.get('model_name', '')} |",
        f"| Execution Time    | {data.get('execution_time', '')} sec |",
        f"| Input Tokens      | {data.get('input_tokens', ''):,} |",
        f"| Output Tokens     | {data.get('output_tokens', ''):,} |",
        f"| Total Tokens      | {data.get('total_tokens', ''):,} |",
        f"| Estimated Cost    | ${data.get('estimated_cost', 0):.6f} USD |",
        f"| Confidence Score  | {confidence}% |",
        f"| Risk Level        | {risk} |",
        "",
        "---",
        "",
        "## Quality & Risk Assessment",
        "",
    ]

    for note in notes:
        lines.append(f"- {note}")

    lines += [
        "",
        "---",
        "",
        "## Technical Appendix",
        "",
        f"| Field              | Value |",
        f"|-------------------|-------|",
        f"| Execution ID       | `{data.get('execution_id', '')}` |",
        f"| Model              | {data.get('model_name', '')} |",
        f"| Execution Time     | {data.get('execution_time', '')} sec |",
        f"| Input Tokens       | {data.get('input_tokens', '')} |",
        f"| Output Tokens      | {data.get('output_tokens', '')} |",
        f"| Total Tokens       | {data.get('total_tokens', '')} |",
        f"| Estimated Cost     | ${data.get('estimated_cost', 0):.6f} USD |",
        "",
        "---",
        "",
        f"**Report Integrity Hash:** `{integrity_hash}`",
        "",
        f"*Generated at {generated_at} by LLMSupervisor v0.1.0*",
    ]

    return "\n".join(lines)


