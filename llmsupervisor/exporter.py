# ── PDF builder ────────────────────────────────────────────────────────────── #

def _build_pdf(
    data: dict[str, Any],
    quality: dict[str, Any],
    integrity_hash: str,
    out_path: str,
) -> None:
    """
    Build a boardroom-ready PDF audit report using reportlab.

    Layout:
        - Dark header bar with title
        - Section blocks with light grey separators
        - Proper font hierarchy (Helvetica family)
        - Page number footer with integrity hash
    """
    try:
        from reportlab.lib import colors
        from reportlab.lib.pagesizes import A4
        from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
        from reportlab.lib.units import mm
        from reportlab.platypus import (
            SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
            HRFlowable, KeepTogether,
        )
        from reportlab.lib.enums import TA_CENTER, TA_LEFT
    except ImportError as exc:
        raise ImportError(
            "reportlab is required for PDF export. "
            "Install it with: pip install reportlab>=4.0.0"
        ) from exc

    generated_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    confidence = quality.get("confidence_score", "N/A")
    risk = quality.get("risk_level", "N/A")
    notes: list[str] = quality.get("notes", [])
    summary: str = quality.get("summary", "")

    # Colour palette
    DARK_BLUE   = colors.HexColor("#1a253c")
    MID_GREY    = colors.HexColor("#7f8c8d")
    LIGHT_GREY  = colors.HexColor("#eaecf0")
    BG_GREY     = colors.HexColor("#f4f6f9")
    GREEN       = colors.HexColor("#27ae60")
    YELLOW      = colors.HexColor("#f39c12")
    RED         = colors.HexColor("#e74c3c")
    WHITE       = colors.white
    BLACK       = colors.HexColor("#2c3e50")

    confidence_color = GREEN if confidence >= 85 else YELLOW if confidence >= 70 else RED
    risk_color = GREEN if risk == "LOW" else YELLOW

    PAGE_W, PAGE_H = A4
    margin = 18 * mm

    styles = getSampleStyleSheet()

    style_body = ParagraphStyle(
        "body", fontName="Helvetica", fontSize=9, textColor=BLACK,
        leading=14, spaceAfter=4,
    )
    style_section_title = ParagraphStyle(
        "section_title", fontName="Helvetica-Bold", fontSize=8,
        textColor=MID_GREY, leading=12, spaceBefore=14, spaceAfter=6,
        textTransform="uppercase",
    )
    style_summary = ParagraphStyle(
        "summary", fontName="Helvetica-Oblique", fontSize=9,
        textColor=BLACK, leading=15, spaceAfter=4,
        leftIndent=8, rightIndent=8,
    )

    # ── Document ──────────────────────────────────────────────────────────── #
    def _on_page(canvas, doc):
        """Draw header bar and footer on every page."""
        canvas.saveState()

        # Header bar
        canvas.setFillColor(DARK_BLUE)
        canvas.rect(0, PAGE_H - 28 * mm, PAGE_W, 28 * mm, fill=1, stroke=0)
        canvas.setFillColor(WHITE)
        canvas.setFont("Helvetica-Bold", 13)
        canvas.drawCentredString(PAGE_W / 2, PAGE_H - 14 * mm, "AI Execution Audit Report")
        canvas.setFont("Helvetica", 8)
        canvas.setFillColor(colors.HexColor("#a0b0c8"))
        canvas.drawCentredString(PAGE_W / 2, PAGE_H - 21 * mm,
                                 f"LLMSupervisor v0.1.0  |  {generated_at}")

        # Footer
        canvas.setFillColor(BG_GREY)
        canvas.rect(0, 0, PAGE_W, 18 * mm, fill=1, stroke=0)
        canvas.setFillColor(MID_GREY)
        canvas.setFont("Helvetica", 7)
        canvas.drawString(margin, 11 * mm,
                          f"Integrity Hash: {integrity_hash[:32]}...")
        canvas.drawRightString(PAGE_W - margin, 11 * mm,
                               f"Page {doc.page}")
        canvas.setFillColor(LIGHT_GREY)
        canvas.rect(margin, 15 * mm, PAGE_W - 2 * margin, 0.3 * mm, fill=1, stroke=0)

        canvas.restoreState()

    doc = SimpleDocTemplate(
        out_path,
        pagesize=A4,
        leftMargin=margin,
        rightMargin=margin,
        topMargin=32 * mm,
        bottomMargin=22 * mm,
        title="AI Execution Audit Report",
        author="LLMSupervisor v0.1.0",
    )

    def _section(title: str) -> list:
        return [
            HRFlowable(width="100%", thickness=0.5, color=LIGHT_GREY,
                       spaceAfter=2, spaceBefore=10),
            Paragraph(title, style_section_title),
        ]

    def _table(rows: list, col_widths=None) -> Table:
        t = Table(rows, colWidths=col_widths or [70 * mm, None])
        t.setStyle(TableStyle([
            ("BACKGROUND",  (0, 0), (-1, 0), BG_GREY),
            ("TEXTCOLOR",   (0, 0), (-1, 0), MID_GREY),
            ("FONTNAME",    (0, 0), (-1, 0), "Helvetica-Bold"),
            ("FONTSIZE",    (0, 0), (-1, 0), 7.5),
            ("FONTNAME",    (0, 1), (-1, -1), "Helvetica"),
            ("FONTSIZE",    (0, 1), (-1, -1), 9),
            ("TEXTCOLOR",   (0, 1), (-1, -1), BLACK),
            ("ROWBACKGROUNDS", (0, 1), (-1, -1), [WHITE, BG_GREY]),
            ("GRID",        (0, 0), (-1, -1), 0.3, LIGHT_GREY),
            ("TOPPADDING",  (0, 0), (-1, -1), 5),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 5),
            ("LEFTPADDING", (0, 0), (-1, -1), 8),
        ]))
        return t

    story = []

    # ── Cover ─────────────────────────────────────────────────────────────── #
    story += _section("Cover")
    story.append(_table([
        ["Field", "Value"],
        ["Execution ID",    str(data.get("execution_id", ""))],
        ["Model Used",      str(data.get("model_name", ""))],
        ["Date & Time",     generated_at],
        ["Generated By",    "LLMSupervisor v0.1.0"],
    ]))
    story.append(Spacer(1, 4 * mm))

    # ── Executive Summary ─────────────────────────────────────────────────── #
    story += _section("Executive Summary")
    story.append(Paragraph(summary, style_summary))
    story.append(Spacer(1, 4 * mm))

    # ── Execution Overview ────────────────────────────────────────────────── #
    story += _section("Execution Overview")
    story.append(_table([
        ["Metric", "Value"],
        ["Model",           str(data.get("model_name", ""))],
        ["Execution Time",  f"{data.get('execution_time', '')} sec"],
        ["Input Tokens",    f"{data.get('input_tokens', 0):,}"],
        ["Output Tokens",   f"{data.get('output_tokens', 0):,}"],
        ["Total Tokens",    f"{data.get('total_tokens', 0):,}"],
        ["Estimated Cost",  f"${data.get('estimated_cost', 0):.6f} USD"],
        ["Confidence Score", f"{confidence}%"],
        ["Risk Level",      str(risk)],
    ]))
    story.append(Spacer(1, 4 * mm))

    # ── Quality & Risk ────────────────────────────────────────────────────── #
    story += _section("Quality & Risk Assessment")
    for note in notes:
        story.append(Paragraph(f"• {note}", style_body))
    story.append(Spacer(1, 4 * mm))

    # ── Technical Appendix ───────────────────────────────────────────────── #
    story += _section("Technical Appendix")
    story.append(_table([
        ["Field", "Value"],
        ["Execution ID",   str(data.get("execution_id", ""))],
        ["Model",          str(data.get("model_name", ""))],
        ["Input Tokens",   str(data.get("input_tokens", ""))],
        ["Output Tokens",  str(data.get("output_tokens", ""))],
        ["Total Tokens",   str(data.get("total_tokens", ""))],
        ["Estimated Cost", f"${data.get('estimated_cost', 0):.6f} USD"],
    ]))
    story.append(Spacer(1, 6 * mm))

    # ── Integrity hash ────────────────────────────────────────────────────── #
    story.append(HRFlowable(width="100%", thickness=0.5, color=LIGHT_GREY,
                            spaceAfter=4, spaceBefore=6))
    story.append(Paragraph(
        f"<font size='7' color='#7f8c8d'>"
        f"<b>Report Integrity Hash:</b> {integrity_hash}</font>",
        style_body,
    ))

    doc.build(story, onFirstPage=_on_page, onLaterPages=_on_page)


# ── File writer ────────────────────────────────────────────────────────────── #

def _write_text(path: str, content: str) -> None:
    """Write a UTF-8 text file."""
    with open(path, "w", encoding="utf-8") as fh:
        fh.write(content)
